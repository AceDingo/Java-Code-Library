//(Display five messages) Write a program that displays Welcome to Java five times.
public class WelcomeToJava_5Times {
	public static void main (String[] args) {
		int i =0; // initializing the increment variable.
		
		// FOR Loop: Incremental count to 5, printing line each count. 
		for (i = 0; i < 5; i++) {
			System.out.println("Welcome to Java Bro, grab a Beer!");
		}
	}

}
